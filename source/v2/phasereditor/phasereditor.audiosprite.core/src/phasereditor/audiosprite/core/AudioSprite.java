package phasereditor.audiosprite.core;

public class AudioSprite implements Comparable<AudioSprite> {
	double start;
	double end;
	private String name;

	public boolean isValid() {
		return getStart() >= 0 && getStart() < getEnd();
	}

	public double duration() {
		return getEnd() - getStart();
	}

	public double getStart() {
		return start;
	}

	public void setStart(double start) {
		this.start = start;
	}

	public double getEnd() {
		return end;
	}

	public void setEnd(double end) {
		this.end = end;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public int compareTo(AudioSprite o) {
		if (o == null) {
			return -1;
		}
		return (int) (start - o.start);
	}
}